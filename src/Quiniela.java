    private void bRealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRealizarActionPerformed
        Random aleatorio= new Random();
        
        
        DefaultListModel modelo=new DefaultListModel();
        DefaultListModel modelo2= (DefaultListModel) listPartidos.getModel();
        
        for (int i = 0; i < modelo2.size(); i++) 
        {
            int ale=aleatorio.nextInt(3-1)+1;
            String resultado="";

            if(ale==1)
            {
                resultado="X";
            }
            else if(ale==2)
            {
                resultado="1";
            }
            else if(ale==3)
            {
                resultado="2";
            }
            String elemento=modelo2.get(i).toString()+"--> "+resultado;
            modelo.addElement(elemento);
            
        }
        listResultados.setModel(modelo);
        listPartidos.setModel(new DefaultListModel());
        
        
        
    }//GEN-LAST:event_bRealizarActionPerformed

