    /**
     * Agrega el texto pasado en la otra ventana al text area
     * @param partido equipos del partido
     */
    public void agregarPartido(String partido)
    {
        modelovacio= new DefaultListModel();
        for (int i = 0; i < listPartidos.getModel().getSize(); i++) 
        {
            modelovacio.addElement(listPartidos.getModel().getElementAt(i));
            
        }
        modelovacio.addElement(partido);
        listPartidos.setModel(modelovacio);
    }
    
    public void eliminarPartido(int n)
    {
        listPartidos.remove(n);
    }
